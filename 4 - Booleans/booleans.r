# Comparação de valores
10 > 9
10 == 9
10 < 9

# Comparação de variáveis
var1 <- 9
var2 <- 23

var1 > var2
var1 == var2
var1 < var2

# Comparação entre tipos de variáveis
class(var1) == class(var2)

# Estruturas condicionais
if (var1 > var2) {
  paste(var1, " é maior do que ", var2)
} else {
  paste(var2, " é maior do que ", var1)
}